# Create a variable weekdays and set it equal to a tuple that contains the days Monday - Friday as strings.
weekdays = ('Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday')

# Create a variable weekends and set it equal to a tuple that contains Saturday and Sunday as strings.
weekends = ('Saturday', 'Sunday')

# Create a variable called days_of_the_week and set it equal to the concatenation of weekdays and weekends.
days_of_the_week = weekdays + weekends

# Printing days_of_the_week will display a tuple containing all the days of the week in order.
print(days_of_the_week)